
def run_ex(person_id)
  puts "How far did person #{person_id} run (in metres)?"
  distance = gets.to_f
  puts "How long (in minutes) did person #{person_id} run take to run #{distance} metres?"
  time = gets.to_f

speed = distance/(time*60)
return speed


end

